#include "ofApp.h"

//--------------------------------------------------------------
void ofApp::setup() {

	ofSetFrameRate(60);
	ofSetWindowTitle("openFrameworks");

	ofBackground(239);
	ofNoFill();
	ofSetRectMode(ofRectMode::OF_RECTMODE_CENTER);
	ofEnableDepthTest();

	this->frame.setMode(ofPrimitiveMode::OF_PRIMITIVE_LINES);

	for (int z = -400; z <= 400; z += 400) {

		this->base_location_list.push_back(glm::vec3(-400, -400, z));
		this->base_location_list.push_back(glm::vec3(0, -400, z));
		this->base_location_list.push_back(glm::vec3(400, -400, z));

		this->base_location_list.push_back(glm::vec3(-400, 0, z));
		this->base_location_list.push_back(glm::vec3(0, 0, z));
		this->base_location_list.push_back(glm::vec3(400, 0, z));

		this->base_location_list.push_back(glm::vec3(-400, 400, z));
		this->base_location_list.push_back(glm::vec3(0, 400, z));
		this->base_location_list.push_back(glm::vec3(400, 400, z));
	}
}

//--------------------------------------------------------------
void ofApp::update() {

	ofSeedRandom(39);

	this->face.clear();
	this->frame.clear();

	float side = 300;
	float speed = 60;
	for(auto& base_location : this->base_location_list) {

		int param_start = ofRandom(100);
		int len = ofRandom(10, 80);
		int gap = 100 - len;
		for (int i = param_start; i < param_start + len; i += 2) {

			glm::vec3 base, next, location;
			base = glm::vec3(this->make_point(side, i), 0);
			next = glm::vec3(this->make_point(side, i + 2), 0);

			location = base + (next - base) / speed * (ofGetFrameNum() % (int)speed);
			this->setBoxToMesh(this->face, this->frame, base_location + location, side / 12.6 * 0.8);
		}

		float p = param_start + len + (gap / speed) * (ofGetFrameNum() % (int)speed);
		auto base = glm::vec3(this->make_point(side, p), 0);
		auto location = base;
		this->setBoxToMesh(this->face, this->frame, base_location + location, side / 12.6 * 0.8);

	}
}

//--------------------------------------------------------------
void ofApp::draw() {

	this->cam.begin();
	ofRotateX(295);

	ofSetColor(139);
	for (auto& base_location : this->base_location_list) {

		ofDrawRectangle(base_location, 300, 300);
	}

	ofSetColor(39);
	this->face.draw();

	ofSetColor(239);
	this->frame.drawWireframe();

	this->cam.end();
}

//--------------------------------------------------------------
void ofApp::setBoxToMesh(ofMesh& face_target, ofMesh& frame_target, glm::vec3 location, float size) {

	this->setBoxToMesh(face_target, frame_target, location, size, size, size);
}

